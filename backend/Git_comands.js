/*

0. git pull origin main -> Actualiza tu rama local con la remota
1. git status -> Ver los archivos modificados o en staged
2. git diff -> ver cambios en carpetas y/o achivos
3. git add ruta_archivo -> agrega el archivo al staged
4. git commit -m "Comentario del cambio"
5. git push -u origin nombre_de_la_rama -> Sube los archivos del staged al remote
6. git checkout -b nombre_de_la_rama -> Crea una nueva rama
7. git checkout -- ./ o git checkout -- ruta_archivo -> Restaura los cambios

*/